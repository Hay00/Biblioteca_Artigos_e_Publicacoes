    private void jbCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbCreateActionPerformed

        String pass = new String(jpfPassword.getPassword());
        if (jtfName.getText().trim().isEmpty() || jtfEmail.getText().trim().isEmpty() || jtfPhone.getText().trim().isEmpty()
                || jtfLogin.getText().trim().isEmpty() || jtfPhone.getText().trim().equals("(  )     -    ")
                || pass.trim().isEmpty()) {

            //Algum campo vazio.
            GUIMessage.error("Please, fill all the text fields!");

        } else if (pass.matches("[0-9]+")) {
            //Caso o cadastro estiver de acordo.

            User usr = new User();
            usr.setName(jtfName.getText());
            usr.setEmail(jtfEmail.getText());
            usr.setPhone(jtfPhone.getText());
            usr.setLogin(jtfLogin.getText());
            usr.setPassword(Integer.parseInt(pass));
            library.addUser(usr);
            dispose();

        } else {
            // Algum campo de texto onde números não são permitidos tem algum número.
            GUIMessage.error("The password field can only contain NUMBERS!");
        }
    }//GEN-LAST:event_jbCreateActionPerformed

