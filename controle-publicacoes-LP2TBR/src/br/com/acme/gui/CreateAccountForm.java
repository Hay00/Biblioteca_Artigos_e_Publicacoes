    private void jtfLoginFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jtfLoginFocusLost
        // Verifica se já existe um usuário com esse login.
        if (!jtfLogin.getText().trim().isEmpty()) {
            if (!library.hasLogin(jtfLogin.getText())) {
                jbCreate.setEnabled(true);
            } else {
                GUIMessage.inf("This login alredy exist!");
                jbCreate.setEnabled(false);
            }
        }
    }//GEN-LAST:event_jtfLoginFocusLost

