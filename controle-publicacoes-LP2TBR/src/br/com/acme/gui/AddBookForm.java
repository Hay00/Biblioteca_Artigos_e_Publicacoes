    /**
     * Salva o book. Método separado do código principal devido sua extensão.
     */
    private void savingBook() {
        Book book = new Book();
        book.setTitle(jtfTitle.getText());
        book.setAuthors(authors);
        book.setYear(Short.parseShort(jtfYear.getText()));
        book.setVolume(Byte.parseByte(jtfVolume.getText()));
        book.setLanguage(jtfLanguage.getText());
        book.setIsbn(Long.parseLong(jtfIsbn.getText()));
        book.setPages(Short.parseShort(jtfPages.getText()));
        // Variáveis do book.

        library.addPublication(book);
        // Adicionando o book na publicação.

        dispose();
    }

