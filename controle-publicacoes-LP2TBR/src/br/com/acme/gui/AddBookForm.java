    private void jbOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbOKActionPerformed
        if (authors.isEmpty()) {
            // Nenhum author foi registrado.
            GUIMessage.error("Please, add the author!");
        }

        // Entra se algum campo não foi preenchido corretamente.
        if (jtfTitle.getText().trim().isEmpty() || jtfYear.getText().trim().isEmpty() || jtfVolume.getText().trim().isEmpty()
                || jtfLanguage.getText().trim().isEmpty() || jtfIsbn.getText().trim().isEmpty() || jtfPages.getText().trim().isEmpty()) {

            // Algum campo vazio.
            GUIMessage.error("Please, fill all the text fields!");

        } else if (jtfYear.getText().trim().matches("[0-9]+") && jtfVolume.getText().trim().matches("[0-9]+") && jtfIsbn.getText().trim().matches("[0-9]+")
                && jtfPages.getText().trim().matches("[0-9]+") && !authors.isEmpty()) {

            // Caso o cadastro estiver de acordo.
            savingBook();

        } else {
            // Algum campo de texto onde números não são permitidos tem algum número.
            GUIMessage.error("Please notice that this fields can only contain NUMBERS:  \nYear\nVolume\nIsbn\nPages");
        }
    }//GEN-LAST:event_jbOKActionPerformed

    private void jtfIsbnFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jtfIsbnFocusLost
        // Verifica se o isbn já existe.

        if (jtfIsbn.getText().trim().matches("[0-9]+")) {
            long num = Long.parseLong(jtfIsbn.getText().trim());
            if (!library.hasIsbn(num)) {
                jbOK.setEnabled(true);
            } else {
                GUIMessage.inf("This ISBN alredy exist!");
                jbOK.setEnabled(false);
            }
        }
    }//GEN-LAST:event_jtfIsbnFocusLost

